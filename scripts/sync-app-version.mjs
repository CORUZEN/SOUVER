import { execSync } from 'node:child_process'
import { mkdirSync, writeFileSync } from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const rootDir = path.resolve(__dirname, '..')
const outputDir = path.join(rootDir, 'src', 'generated')
const outputFile = path.join(outputDir, 'app-version.ts')

function getCommitCount() {
  try {
    const result = execSync('git rev-list --count HEAD', {
      cwd: rootDir,
      stdio: ['ignore', 'pipe', 'ignore'],
      encoding: 'utf-8',
    }).trim()

    const parsed = Number.parseInt(result, 10)
    if (Number.isFinite(parsed) && parsed >= 0) {
      return parsed
    }
  } catch {
    // Fallback para ambientes sem metadados do git.
  }

  return 0
}

const commitCount = getCommitCount()
const appVersion = `1.00.${commitCount}`
const generatedAt = new Date().toISOString()

const content = `// Auto-generated by scripts/sync-app-version.mjs
// Generated at ${generatedAt}
export const APP_VERSION = '${appVersion}'
export const APP_VERSION_LABEL = 'v${appVersion}'
`

mkdirSync(outputDir, { recursive: true })
writeFileSync(outputFile, content, 'utf-8')

console.log(`[version:sync] APP_VERSION=${appVersion} (commits=${commitCount})`)
